using System;
using System.Windows.Forms;

namespace EuroText2
{
    //-------------------------------------------------------------------------------------------------------------------------------
    //-------------------------------------------------------------------------------------------------------------------------------
    //-------------------------------------------------------------------------------------------------------------------------------
    public partial class FrmMultSetGroup : Form
    {
        public string selectedTextGroup;

        //-------------------------------------------------------------------------------------------------------------------------------
        public FrmMultSetGroup()
        {
            InitializeComponent();
        }

        //-------------------------------------------------------------------------------------------------------------------------------
        private void FrmMultSetGroup_Load(object sender, EventArgs e)
        {
            CommonFunctions.GroupsToCombobox(comboBox1);
        }

        //-------------------------------------------------------------------------------------------------------------------------------
        private void Button_Ok_Click(object sender, EventArgs e)
        {
            DialogResult = DialogResult.OK;
            selectedTextGroup = comboBox1.SelectedItem.ToString();
            Close();
        }

        //-------------------------------------------------------------------------------------------------------------------------------
        private void Button_Cancel_Click(object sender, EventArgs e)
        {
            DialogResult = DialogResult.Cancel;
            Close();
        }
    }

